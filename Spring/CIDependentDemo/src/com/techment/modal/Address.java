package com.techment.modal;

public class Address {

	String cityName;
	String streetName;
	int zipCode;
	String stateName;

	public Address(String cityName, String streetName, int zipCode, String stateName) {
		super();
		this.cityName = cityName;
		this.streetName = streetName;
		this.zipCode = zipCode;
		this.stateName = stateName;
	}
	
}
